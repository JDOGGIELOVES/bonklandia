#!/usr/bin/env node
/**
 * Copy Bonga Bonk Miner treasury → Bonklandia .env.local
 *
 * Usage:
 *   node scripts/sync-bonk-miner-treasury.mjs
 *   node scripts/sync-bonk-miner-treasury.mjs --miner-dir C:\Users\geohi\bonga-bonk-miner
 */
import { readFileSync, existsSync, writeFileSync } from 'fs';
import { join, resolve } from 'path';
import { Keypair } from '@solana/web3.js';
import bs58 from 'bs58';

const minerDir = process.argv.includes('--miner-dir')
  ? process.argv[process.argv.indexOf('--miner-dir') + 1]
  : resolve(process.env.USERPROFILE ?? process.env.HOME ?? '', 'bonga-bonk-miner');

const keyPath = join(minerDir, '.treasury', 'treasury-keypair.json');

if (!existsSync(keyPath)) {
  console.error(`\nTreasury key not found at:\n  ${keyPath}\n`);
  console.error('In Bonk Miner folder run: npm run treasury:setup');
  process.exit(1);
}

const secret = Uint8Array.from(JSON.parse(readFileSync(keyPath, 'utf8')));
const kp = Keypair.fromSecretKey(secret);
const publicKey = kp.publicKey.toBase58();
const privateKeyB58 = bs58.encode(secret);

const lines = [
  '# Shared treasury — Bonklandia / Bonk Miner / GrokSight (bonklandia.com)',
  '# Generated by scripts/sync-bonk-miner-treasury.mjs — do NOT commit',
  '',
  'NEXT_PUBLIC_SOLANA_NETWORK=mainnet-beta',
  `NEXT_PUBLIC_TREASURY_PUBLIC_KEY=${publicKey}`,
  `TREASURY_PUBLIC_KEY=${publicKey}`,
  `BONGA_TREASURY_SECRET_KEY=${privateKeyB58}`,
  '',
  '# Optional Helius / dedicated RPC:',
  '# SOLANA_RPC_URL=https://mainnet.helius-rpc.com/?api-key=YOUR_KEY',
  '# NEXT_PUBLIC_SOLANA_RPC_URL=https://mainnet.helius-rpc.com/?api-key=YOUR_KEY',
  '',
];

const outPath = join(process.cwd(), '.env.local');
writeFileSync(outPath, lines.join('\n'), 'utf8');

console.log('\n=== Bonk Miner treasury → Bonklandia ===\n');
console.log('Treasury public:', publicKey);
console.log('Wrote:', outPath);
console.log('\nRestart `npm run dev` to load .env.local\n');